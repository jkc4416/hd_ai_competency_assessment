x = 26
d = [0] * 30001 

def make_one(x):
    for i in range(2, x + 1):
        # 1. 1을 빼는 경우 (기본값)
        d[i] = d[i-1] + 1
        
        # 2. 2로 나누어 떨어지는지 확인 (if 사용)
        if i % 2 == 0:
            d[i] = min(d[i], d[i//2] + 1)
            
        # 3. 3으로 나누어 떨어지는지 확인 (elif가 아니라 if 사용!)
        # 이유: 6같은 숫자는 2로도, 3으로도 나눠지므로 둘 다 비교해봐야 최솟값을 찾음
        if i % 3 == 0:
            d[i] = min(d[i], d[i//3] + 1)
        
        # 4. 5로 나누어 떨어지는지 확인 (들여쓰기 밖으로 꺼내기)
        if i % 5 == 0:
            d[i] = min(d[i], d[i//5] + 1)
            
    return d[x]

print(f"숫자 {x}를 1로 만드는 최소 연산 횟수: {make_one(x)}")